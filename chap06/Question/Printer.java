package chap06;

public class Printer {

	static void println(int put) {
		System.out.println(put);
	}
	static void println(boolean put) {
		System.out.println(put);
	}
	static void println(double put) {
		System.out.println(put);
	}
	static void println(String put) {
		System.out.println(put);
	}

}
